#!/bin/bash
# inject-handoff-on-restart.sh — Re-inject handoff after /clear or compaction
#
# Fires on SessionStart (matcher: "clear|compact"). Checks for handoff files
# and outputs their content as additionalContext so Claude sees the handoff
# as its first context in the fresh/compacted session.
#
# Priority:
#   1. handoff.md (Claude-generated, richer context)
#   2. auto-handoff.md (mechanically extracted from transcript)

set -euo pipefail

HANDOFF_DIR="$HOME/.claude/handoffs"
CLAUDE_HANDOFF="$HANDOFF_DIR/handoff.md"
AUTO_HANDOFF="$HANDOFF_DIR/auto-handoff.md"

# Check for Claude-generated handoff first (higher quality)
if [ -f "$CLAUDE_HANDOFF" ]; then
  echo "[SESSION CONTINUITY] Handoff document detected. Resuming from previous context."
  echo ""
  cat "$CLAUDE_HANDOFF"
  echo ""
  echo "---"
  echo "The above handoff was generated by Claude in a previous session/context."
  echo "Continue working from where it left off. Check 'Next Steps' and proceed."
  echo "After reading this, confirm you understand the context before continuing."

  # Archive the handoff so it doesn't re-inject on the NEXT clear
  mv "$CLAUDE_HANDOFF" "$HANDOFF_DIR/handoff.$(date +%s).used.md" 2>/dev/null || true
  exit 0
fi

# Fall back to auto-generated handoff
if [ -f "$AUTO_HANDOFF" ]; then
  echo "[SESSION CONTINUITY] Auto-generated handoff detected (from transcript)."
  echo ""
  cat "$AUTO_HANDOFF"
  echo ""
  echo "---"
  echo "This handoff was mechanically extracted from the transcript before compaction."
  echo "It may be less detailed than a Claude-generated handoff. Review the modified"
  echo "files and recent requests above, then continue working."

  # Archive
  mv "$AUTO_HANDOFF" "$HANDOFF_DIR/auto-handoff.$(date +%s).used.md" 2>/dev/null || true
  exit 0
fi

# No handoff found — this is a normal clear/compact, no action needed
